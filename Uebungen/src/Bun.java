public class Bun {

    private int preis; // Preis in Cent
    private String sorte;

    public Bun(int preis, String sorte) {
        this.preis = preis;
        this.sorte = sorte;
    }

    public int getPreis() {
        return preis;
    }

    public String getSorte() {
        return sorte;
    }

}
